# 08 – Escreva um programa que leia um valor em metros e o exiba convertido em 
# milímetros. 1 metro é igual a 100 cm que é igual a 10 mm.

print("Programa que converte metros em milímetros.")

metros = float(input("Informe a medida em metros: "))

milimetros = metros * 1000

print(f"Valor em milímetros: {milimetros}")
